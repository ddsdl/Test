a, b = map(int, input().split(" "))

print(f"a = {a}")
print(f"b = {b}")
# == print(f"a = {a}\nb = {b}")

# map 함수 활용
# map : 각 요소들에 특정한 함수를 적용시킬 때 쓰는 함수
# 1개의 입력이 아니라 , 여러개가 입력된 경우 각각 요소에 적용시키고 싶을때
